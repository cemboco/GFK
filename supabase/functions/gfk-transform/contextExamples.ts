export const getContextExamples = (contextKey) => {
  return contextExamples[contextKey] || [];
};

export const getContextPrompt = (contextKey) => {
  const examples = getContextExamples(contextKey);
  
  if (examples.length === 0) {
    return '';
  }

  const exampleTexts = examples.map((example, index) => {
    return `Beispiel ${index + 1}:
Aussage: "${example.original_text}"
Umformulierung: "${example.output.reformulated_text}"
(Beobachtung: ${example.output.observation} | Gefühl: ${example.output.feeling} | Bedürfnis: ${example.output.need} | Bitte: ${example.output.request})`;
  }).join('\n\n');

  const contextLabel = contextKey.charAt(0).toUpperCase() + contextKey.slice(1);

  return `\n\nBeispiele für Umformulierungen im Kontext "${contextLabel}":\n${exampleTexts}\n\nBitte verwende einen ähnlichen Stil und Ton für die Transformation.`;
}; 