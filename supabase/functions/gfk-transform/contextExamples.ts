export const getContextExamples = (contextKey, lang = 'de') => {
  return contextExamples[contextKey]?.[lang] || [];
};

export const getContextPrompt = (contextKey, lang = 'de') => {
  const examples = getContextExamples(contextKey, lang);
  
  if (examples.length === 0) {
    return '';
  }

  const exampleTexts = examples.map((example, index) => {
    return (lang === 'en'
      ? `Example ${index + 1}:
Statement: "${example.original_text}"
Reformulation: "${example.output.reformulated_text}"
(Observation: ${example.output.observation} | Feeling: ${example.output.feeling} | Need: ${example.output.need} | Request: ${example.output.request})`
      : `Beispiel ${index + 1}:
Aussage: "${example.original_text}"
Umformulierung: "${example.output.reformulated_text}"
(Beobachtung: ${example.output.observation} | Gefühl: ${example.output.feeling} | Bedürfnis: ${example.output.need} | Bitte: ${example.output.request})`
    );
  }).join('\n\n');

  const contextLabel = contextKey.charAt(0).toUpperCase() + contextKey.slice(1);

  return (lang === 'en'
    ? `\n\nExamples of reformulations in the context of "${contextLabel}":\n${exampleTexts}\n\nPlease use a similar style and tone for the transformation.`
    : `\n\nBeispiele für Umformulierungen im Kontext "${contextLabel}":\n${exampleTexts}\n\nBitte verwende einen ähnlichen Stil und Ton für die Transformation.`
  );
}; 